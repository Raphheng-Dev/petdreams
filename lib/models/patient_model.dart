class Patient {
  final String name;
  final int age;
  final String weight;
  final String specie;
  final String breed;
  final String sex;
  final String color;

  Patient({
    required this.weight,
    required this.specie,
    required this.breed,
    required this.sex,
    required this.color,
    required this.name,
    required this.age,
  });
}
